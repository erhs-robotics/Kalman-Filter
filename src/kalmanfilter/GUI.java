    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        float move = Float.parseFloat(jTextField1.getText());
        float turn = (float) Math.toRadians(Float.parseFloat(jTextField2.getText()));
        try {
            robot.move(turn, move);
            double measure_x = robot.x + robot.random.nextGaussian();
            double measure_y = (float) robot.y + robot.random.nextGaussian();
            double measure_orient = (float) robot.orientation + robot.random.nextGaussian();
            kfilter.filter(measure_x, measure_y, measure_orient, .1);
            System.out.println(robot.x + " " + kfilter.x.getArray()[0][0]);
            System.out.println(robot.y + " " + kfilter.x.getArray()[1][0]);
            System.out.println(robot.orientation + " " + kfilter.x.getArray()[2][0]);
            System.out.println("***********************************************");
        } catch (Exception ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.repaint();
    }//GEN-LAST:event_jButton2ActionPerformed

